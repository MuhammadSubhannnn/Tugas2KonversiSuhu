    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = jTextField1.getText().trim(); // Menghapus spasi di awal dan akhir

        // Periksa apakah input kosong
        if (input.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan nilai suhu yang valid.");
            return; // Keluar dari metode jika input kosong
        }

        double suhu;
        try {
            suhu = Double.parseDouble(input);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Masukkan nilai suhu yang valid.");
            return; // Keluar dari metode jika input tidak dapat dikonversi
        }

        String skalaAwal = (String) jComboBox1.getSelectedItem();
        String skalaTujuan = "";

        // Tentukan skala tujuan dari JRadioButton yang dipilih
        if (jRadioButton1.isSelected()) {
            skalaTujuan = "Celcius";
        } else if (jRadioButton2.isSelected()) {
            skalaTujuan = "Fahrenheit";
        } else if (jRadioButton3.isSelected()) {
            skalaTujuan = "Kelvin";
        } else if (jRadioButton4.isSelected()) {
            skalaTujuan = "Reamur";
        }

        // Jika skala awal dan skala tujuan tidak dipilih, gunakan default konversi dari Celcius ke Fahrenheit
        if (skalaAwal.equals("Skala Suhu") && skalaTujuan.isEmpty()) {
            skalaAwal = "Celcius";
            skalaTujuan = "Fahrenheit";
        } else if (skalaAwal.equals("Skala Suhu") || skalaTujuan.isEmpty()) {
            // Jika salah satu skala tidak dipilih, tampilkan pesan kesalahan
            JOptionPane.showMessageDialog(null, "Pilih skala suhu awal dan tujuan yang benar.");
            return; // Keluar dari metode jika skala tidak valid
        }

        double hasil = konversiSuhu(suhu, skalaAwal, skalaTujuan);
        jLabel3.setText("Hasil: " + hasil);
    }//GEN-LAST:event_jButton1ActionPerformed

