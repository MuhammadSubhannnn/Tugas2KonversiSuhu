    private double konversiSuhu(double suhu, String skalaAwal, String skalaTujuan) {
       double hasil = 0;

    // Logika konversi suhu
    if (skalaAwal.equals("Celcius")) {
        if (skalaTujuan.equals("Fahrenheit")) {
            hasil = (suhu * 9 / 5) + 32;
        } else if (skalaTujuan.equals("Kelvin")) {
            hasil = suhu + 273.15;
        } else if (skalaTujuan.equals("Reamur")) {
            hasil = suhu * 4 / 5;
        } else {
            hasil = suhu; // jika skala tujuan sama
        }
    } else if (skalaAwal.equals("Fahrenheit")) {
        if (skalaTujuan.equals("Celcius")) {
            hasil = (suhu - 32) * 5 / 9;
        } else if (skalaTujuan.equals("Kelvin")) {
            hasil = (suhu - 32) * 5 / 9 + 273.15;
        } else if (skalaTujuan.equals("Reamur")) {
            hasil = (suhu - 32) * 4 / 9;
        } else {
            hasil = suhu; // jika skala tujuan sama
        }
    } else if (skalaAwal.equals("Kelvin")) {
        if (skalaTujuan.equals("Celcius")) {
            hasil = suhu - 273.15;
        } else if (skalaTujuan.equals("Fahrenheit")) {
            hasil = (suhu - 273.15) * 9 / 5 + 32;
        } else if (skalaTujuan.equals("Reamur")) {
            hasil = (suhu - 273.15) * 4 / 5;
        } else {
            hasil = suhu; // jika skala tujuan sama
        }
    } else if (skalaAwal.equals("Reamur")) {
        if (skalaTujuan.equals("Celcius")) {
            hasil = suhu * 5 / 4;
        } else if (skalaTujuan.equals("Fahrenheit")) {
            hasil = (suhu * 9 / 4) + 32;
        } else if (skalaTujuan.equals("Kelvin")) {
            hasil = (suhu * 5 / 4) + 273.15;
        } else {
            hasil = suhu; // jika skala tujuan sama
        }
    }

    return hasil;
}

